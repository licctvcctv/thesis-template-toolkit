def main():
    with open(INPUT, "r", encoding="utf-8") as f:
        content = f.read()

    applied = 0
    failed = []

    for idx, (old, new) in enumerate(REPLACEMENTS, 1):
        if old in content:
            content = content.replace(old, new)
            applied += 1
        else:
            failed.append(idx)

    with open(INPUT, "w", encoding="utf-8") as f:
        f.write(content)

    print(f"Total replacements defined: {len(REPLACEMENTS)}")
    print(f"Successfully applied: {applied}")
    if failed:
        print(f"Failed (not found): {failed}")
    else:
        print("All replacements matched and applied successfully.")


if __name__ == "__main__":
    main()
