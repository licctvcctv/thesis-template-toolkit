def process_file(filepath, replacements):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    count = 0
    for old, new in replacements:
        if old in content:
            content = content.replace(old, new)
            count += 1
        # else: skip silently

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)

    return count

