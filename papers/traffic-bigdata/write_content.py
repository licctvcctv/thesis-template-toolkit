from __future__ import annotations

import json
from pathlib import Path


HERE = Path(__file__).resolve().parent


def dump(name: str, data):
    (HERE / name).write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")


def text(value: str):
    return {"text": value}


def image(filename: str, caption: str, width_cm: float = 12.0):
    return {"image": f"images/{filename}", "caption": caption, "width_cm": width_cm}


def table(caption: str, headers: list[str], rows: list[list[str]], widths: list[float] | None = None):
    data = {"caption": caption, "headers": headers, "rows": rows}
    if widths:
        data["col_widths"] = widths
    return {"table": data}


def code(caption: str, value: str):
    return {"caption": caption, "code": value}


def section(title: str, content: list, subsections: list | None = None):
    data = {"title": title, "content": content}
    if subsections:
        data["subsections"] = subsections
    return data


